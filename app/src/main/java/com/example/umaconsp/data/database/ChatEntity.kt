package com.example.umaconsp.data.database

import androidx.room.Entity
import androidx.room.PrimaryKey

/**
 * Сущность (Entity) для таблицы чатов.
 * Отражает структуру строки в таблице "chats".
 *
 * Каждый чат имеет уникальный идентификатор (id), название (title),
 * последнее сообщение (lastMessage) и время последнего сообщения (lastMessageTime).
 *
 * Сообщения чата хранятся в отдельной таблице "messages" и связаны с чатом через внешний ключ.
 */
@Entity(tableName = "chats")
data class ChatEntity(
    /**
     * Уникальный идентификатор чата.
     * Является первичным ключом таблицы.
     * Генерируется в коде (обычно UUID.randomUUID().toString()).
     */
    @PrimaryKey
    val id: String,

    /**
     * Название чата.
     * По умолчанию "Чат N", где N — порядковый номер.
     * Пользователь может переименовать чат.
     */
    val title: String,

    /**
     * Текст последнего сообщения в чате (обрезанный до 50 символов).
     * Используется для предварительного просмотра в списке чатов.
     * Может быть null, если чат не содержит сообщений.
     */
    val lastMessage: String?,

    /**
     * Временная метка последнего сообщения в чате (Unix timestamp в миллисекундах).
     * Используется для сортировки чатов в списке (от новых к старым).
     * Обновляется при каждом добавлении сообщения.
     */
    val lastMessageTime: Long
)