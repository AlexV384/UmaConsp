package com.example.umaconsp.data.database

import androidx.room.Entity
import androidx.room.ForeignKey
import androidx.room.Index
import androidx.room.PrimaryKey

/**
 * Сущность (Entity) для таблицы сообщений.
 * Отражает структуру строки в таблице "messages".
 *
 * Каждое сообщение принадлежит одному чату (chatId) и может быть либо от пользователя (isUser = true),
 * либо от ассистента (isUser = false).
 *
 * Сообщения хранятся в порядке возрастания timestamp для правильной хронологии.
 *
 * Поле thinking используется только для пользовательских сообщений (isUser = true) и содержит
 * "размышления" модели, которые были отправлены в процессе генерации ответа.
 *
 * Поле imageUrls хранит пути к изображениям в виде строки, где URL разделены запятыми.
 * Пример: "content://media/external/images/1,content://media/external/images/2"
 */
@Entity(
    tableName = "messages",                              // имя таблицы в БД
    indices = [Index(value = ["chatId"])],               // индекс для ускорения запросов по chatId
    foreignKeys = [                                      // внешние ключи
        ForeignKey(
            entity = ChatEntity::class,                  // родительская таблица
            parentColumns = ["id"],                      // столбец в родительской таблице
            childColumns = ["chatId"],                   // столбец в дочерней таблице
            onDelete = ForeignKey.CASCADE                // при удалении чата удалить все его сообщения
        )
    ]
)
data class MessageEntity(
    /**
     * Уникальный идентификатор сообщения.
     * Является первичным ключом таблицы.
     * Генерируется в коде (обычно UUID.randomUUID().toString()).
     */
    @PrimaryKey
    val id: String,

    /**
     * Идентификатор чата, которому принадлежит сообщение.
     * Внешний ключ, ссылается на таблицу chats (поле id).
     * При удалении чата все сообщения удаляются автоматически (CASCADE).
     */
    val chatId: String,

    /**
     * Текст сообщения.
     * Для ответов ассистента может быть пустым (если в процессе генерации), но в конечном итоге содержит полный ответ.
     * Для пользовательских сообщений всегда содержит введённый текст.
     */
    val text: String,

    /**
     * Флаг, указывающий, является ли сообщение от пользователя (true) или от ассистента (false).
     * Используется для стилизации пузырьков в UI.
     */
    val isUser: Boolean,

    /**
     * Список URL (или URI) изображений, прикреплённых к сообщению.
     * Хранится как строка с разделителем запятая. Пустая строка означает отсутствие изображений.
     * Пример: "content://media/external/images/1,content://media/external/images/2"
     */
    val imageUrls: String,

    /**
     * Временная метка создания сообщения (Unix timestamp в миллисекундах).
     * Используется для сортировки сообщений в хронологическом порядке.
     * Значение по умолчанию устанавливается при создании объекта.
     */
    val timestamp: Long = System.currentTimeMillis(),

    /**
     * Поле для хранения "размышлений" (thinking) модели.
     * Используется только для пользовательских сообщений.
     * Содержит промежуточные рассуждения модели, которые отправляются по SSE перед основным ответом.
     * Может быть null, если размышлений нет или сообщение от ассистента.
     */
    val thinking: String? = null
)