package com.example.umaconsp.domain.model

/**
 * Модель сообщения, используемая в слое представления (UI).
 * Представляет собой неизменяемый (immutable) data class, содержащий все данные,
 * необходимые для отображения сообщения в чате.
 *
 * Сообщения бывают двух типов: от пользователя (isUser = true) и от ассистента (isUser = false).
 * Изображения могут прикрепляться только к сообщениям пользователя.
 *
 * Поле thinking (размышления) используется исключительно для сообщений пользователя.
 * Оно содержит промежуточные рассуждения модели, которые отображаются в сворачиваемом блоке
 * под сообщением пользователя. Для сообщений ассистента поле thinking всегда null.
 *
 * @property id Уникальный идентификатор сообщения (генерируется как UUID.randomUUID().toString())
 * @property text Текст сообщения. Для ответов ассистента может обновляться постепенно в процессе генерации
 * @property isUser Признак сообщения от пользователя (true) или от ассистента (false)
 * @property imageUrls Список URL или URI прикреплённых изображений. По умолчанию пустой список.
 *                     Используется только для сообщений пользователя.
 * @property thinking Текст размышлений модели (только для сообщений пользователя). Может быть null.
 */
data class Message(
    val id: String,
    val text: String,
    val isUser: Boolean,
    val imageUrls: List<String> = emptyList(),
    val thinking: String? = null
)